#include "../includes/philo.h"

int	get_time(void)
{
	struct timeval	tv;
	int				time;

	if (gettimeofday(&tv, NULL) == -1)
		return (-1);
	time = (tv.tv_sec * 1000) + (tv.tv_usec / 1000);
	return (time);
}
